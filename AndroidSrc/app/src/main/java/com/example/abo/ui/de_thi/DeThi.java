package com.example.abo.ui.de_thi;

public class DeThi {
    private String TenDeThi;
    private int MaDeThi;

    public DeThi(String tenDeThi, int maDeThi) {
        TenDeThi = tenDeThi;
        MaDeThi = maDeThi;
    }

    public String getTenDeThi() {
        return TenDeThi;
    }

    public void setTenDeThi(String tenDeThi) {
        TenDeThi = tenDeThi;
    }

    public int getMaDeThi() {
        return MaDeThi;
    }

    public void setMaDeThi(int maDeThi) {
        MaDeThi = maDeThi;
    }
}
